package ir.ac.kntu.cs2d.logic;

public class MyObject {
    private double x;
    private double y;
    private double width;
    private double height;
    private String color;

    public MyObject(double x, double y, double width, double height, String color){
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
        this.color = color;
    }
}
